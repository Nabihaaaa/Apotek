    @Override
    public void tableDisplay() {
      
        try {
            //kolom
            DefaultTableModel tb = new DefaultTableModel();
            tb.addColumn("ID");
            tb.addColumn("Nama");
            tb.addColumn("Jabatan");
            tb.addColumn("NoTelp");
            tb.addColumn("Gaji");
            jTable_Pegawai.setModel(tb);
            
            //get data
            res = stm.executeQuery("select * from Karyawan");
            while (res.next()) {
                tb.addRow(new Object[]{
                    res.getString("ID"),
                    res.getString("Nama"),
                    res.getString("Jabatan"),
                    res.getString("NoTelp"),
                    res.getString("Gaji"),
                });   
            }
        } catch (SQLException ex) {
            Logger.getLogger(Pegawai.class.getName()).log(Level.SEVERE, null, ex);
        }
    
    }

